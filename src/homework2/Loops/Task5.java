package homework2.Loops;

public class Task5 {
    public static void main(String[] args) {
        // ---------------------------------------------------------
        // Задача 5
        //    -	Да се изведат на екрана числата от 1 до 50 в обратен ред.
        // ---------------------------------------------------------

        for (int i = 50; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
